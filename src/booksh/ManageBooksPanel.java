    private void tbl_booklistMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_booklistMouseClicked
        // TODO add your handling code here:
        int sec = tbl_booklist.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tbl_booklist.getModel();
        String name = (String)model.getValueAt(sec, 0);
        String id = (String)model.getValueAt(sec, 1);
        String author = (String)model.getValueAt(sec, 2);
        String price = (String)model.getValueAt(sec, 3);
        String quantity = (String)model.getValueAt(sec, 5);

        txt_bookname.setText(name);
        txt_bookid.setText(id);
        txt_bookauthor.setText(author);
        txt_bookprice.setText(price);
        txt_bookquantity.setText(quantity);
    }//GEN-LAST:event_tbl_booklistMouseClicked


